public class Fibonachi {

    private int N;
    private int fib;

    Fibonachi(int N, int fib) {
        this.N = N;
        this.fib = fib;
    }

    public int getN() {
        return N;
    }

    public void setN(int n) {
        N = n;
    }

    public int getFib() {
        return fib;
    }
    public void setFib(int fib) {
        this.fib = fib;
    }

}
